package common;

public class FloatDoubleExample {
	public static void main(String[] args) {
		double var1 = 3.14;
//		float var2 = 3.14;
		float var3 = 3.14f;
		double var4 = 0.123456789;
		float var5 = 0.123456789f;
		
		System.out.println("v1 " + var1);
//		System.out.println("v2 " + var2);
		System.out.println("v3 " + var3);
		System.out.println("v4 " + var4);
		System.out.println("v5 " + var5);
		
		int var6 = 3000000;
		double var7 = 3e6;
		float var8 = 3e6f;
		double var9 = 2e-3;
		System.out.println("v6 " + var6);
		System.out.println("v7 " + var7);
		System.out.println("v8 " + var8);
		System.out.println("v9 " + var9);
		
	}
}
